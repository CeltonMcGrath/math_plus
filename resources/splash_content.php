<p>Welcome to the registration system for the Math+ programs offered by the Department of Mathematics at the University of Toronto.</p>

<br />

<p>The purpose of this site is to make it simple and easy for you to sign up for programs offered.</p>

<br />

<p>The “Account” tab above has information on your account, such as:
	<ul>
	<li>Email Address and Preference</li>
	<li>Parent/Guardian Information</li>
	<li>Student Information and Programs</li>
	<li>Password Changes</li>
	</ul>
</p>

<br />

<p>The “Cart” tab has programs you have selected.  The prices for each program are listed beside.  If you have applied for a bursary please wait until you hear back from us to register for a program as you will not be reimbursed the difference if you pay full price.</p?

<br />

<p>The “FAQ” tab has a number of Frequently Asked Questions regarding programs, registrations, bursaries and refunds.</p>

<br />

<p>When you are done each session please make sure you click the “Logout” button and close your browser.</p>

<br />

<p>We look forward to seeing you at our programs!</p>